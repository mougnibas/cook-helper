package fr.mougnibas.cookhelper.shoplist.microprofile.health;

import javax.enterprise.context.ApplicationScoped;

import org.eclipse.microprofile.health.Health;
import org.eclipse.microprofile.health.HealthCheck;
import org.eclipse.microprofile.health.HealthCheckResponse;

/**
 * Implementation of Eclipse MicroProfile Health Check 1.0.
 */
@Health
@ApplicationScoped
public class SuccessfulCheck implements HealthCheck {

  @Override
  public HealthCheckResponse call() {
    return HealthCheckResponse.named("successful-check").up().build();
  }
}
